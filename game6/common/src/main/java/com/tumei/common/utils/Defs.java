	public static String getColorString(int grade, String name) {
		String fm = "%s";
		switch (grade) {
			case 1:
				fm = 绿色字段;
				break;
			case 2:
				fm = 蓝色字段;
				break;
			case 3:
				fm = 紫色字段;
				break;
			case 4:
				fm = 橙色字段;
				break;
			case 5:
				fm = 红色字段;
				break;
		}
		return String.format(fm, name);
	}

